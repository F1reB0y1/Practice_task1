#include <stdio.h>   // Підключаємо бібліотеку для вводу/виводу
#include <string.h>  // Підключаємо бібліотеку для роботи з рядками

// Функція для знаходження першого входження підрядка в рядок
int find_substring(const char *str, const char *substr) {
    int i, j; // Змінні для циклів

    // Проходимо по кожному символу основного рядка
    for (i = 0; str[i] != '\0'; i++) {
        // Проходимо по кожному символу підрядка
        for (j = 0; substr[j] != '\0'; j++) {
            // Якщо символи не збігаються, виходимо з внутрішнього циклу
            if (str[i + j] != substr[j]) {
                break; // Виходимо з циклу, якщо символи не співпадають
            }
        }
        // Якщо досягли кінця підрядка, значить знайдено входження
        if (substr[j] == '\0') {
            return i; // Повертаємо індекс першого входження
        }
    }
    return -1; // Якщо підрядок не знайдено, повертаємо -1
}

int main() {
    char str[100]; // Змінна для зберігання основного рядка
    char substr[100]; // Змінна для зберігання підрядка

    // Запитуємо у користувача основний рядок
    printf("Enter original string: ");
    fgets(str, sizeof(str), stdin); // Зчитуємо основний рядок з консолі

    // Запитуємо у користувача підрядок
    printf("Enter substring: ");
    fgets(substr, sizeof(substr), stdin); // Зчитуємо підрядок з консолі

    // Видаляємо символ нового рядка з підрядка, якщо він є
    substr[strcspn(substr, "\n")] = '\0'; // Змінюємо символ нового рядка на символ закінчення рядка

    // Викликаємо функцію для знаходження підрядка
    int index = find_substring(str, substr); // Зберігаємо результат

    // Виводимо результат
    if (index != -1) {
        printf("Substring found at index: %d\n", index); // Виводимо індекс, якщо підрядок знайдено
    } else {
        printf("Substring not found.\n"); // Виводимо повідомлення, якщо підрядок не знайдено
    }

    return 0; // Повертаємо 0 для успішного завершення програми
}